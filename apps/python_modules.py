import random


def add(a, b):
    return a+b


def subtract(a, b):
    return a-b


def generate_random_number(start, end):
    return random.randint(start, end)

def multiply(a, b):
    return a*b

def remainder(a, b):
    return a%b

def division(a, b):
    return a/b

def power(a, b):
    return a**b

def floor(a, b):
    return a//b

